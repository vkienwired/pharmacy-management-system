-- Dumping structure for procedure quan_ly_nha_thuoc.sp_bao_cao_doanh_thu_thang
DELIMITER //
CREATE PROCEDURE `sp_bao_cao_doanh_thu_thang`(
  IN p_nam INT,
  IN p_thang INT
)
BEGIN
  SELECT
    DATE_FORMAT(hd.thoi_gian, '%Y-%m') AS thang,
    COUNT(hd.id_hoa_don) AS so_hoa_don,
    SUM(hd.tong_tien) AS doanh_thu,
    ROUND(AVG(hd.tong_tien), 2) AS tb_tien_moi_hoa_don
  FROM hoa_don hd
  WHERE YEAR(hd.thoi_gian) = p_nam
    AND MONTH(hd.thoi_gian) = p_thang
  GROUP BY DATE_FORMAT(hd.thoi_gian, '%Y-%m');
END//
