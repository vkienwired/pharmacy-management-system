SET @OLDTMP_SQL_MODE=@@SQL_MODE, SQL_MODE='';
DELIMITER //
CREATE TRIGGER `tg_ct_hoa_don_bi`
BEFORE INSERT ON `ct_hoa_don`
FOR EACH ROW
BEGIN
  DECLARE v_ton INT;
  DECLARE v_hsd DATE;

  IF NEW.so_luong_ban <= 0 THEN
    SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Số lượng bán phải > 0.';
  END IF;
  IF NEW.don_gia_ban < 0 THEN
    SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Đơn giá bán phải >= 0.';
  END IF;
  IF NEW.giam_gia_phan_tram < 0 OR NEW.giam_gia_phan_tram > 100 THEN
    SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Giảm giá phải 0..100.';
  END IF;

  SELECT `so_luong_ton`, `han_su_dung`
    INTO v_ton, v_hsd
  FROM `lo_thuoc`
  WHERE `id_lo_thuoc` = NEW.id_lo_thuoc;

  IF v_hsd < CURRENT_DATE() THEN
    SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Không được bán lô đã hết hạn.';
  END IF;
  IF v_ton < NEW.so_luong_ban THEN
    SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Tồn kho không đủ để bán.';
  END IF;

  SET NEW.thanh_tien =
    (NEW.so_luong_ban * NEW.don_gia_ban) * (1 - (NEW.giam_gia_phan_tram/100));
END//
DELIMITER ;
SET SQL_MODE=@OLDTMP_SQL_MODE;

