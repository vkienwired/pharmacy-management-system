DROP TABLE IF EXISTS `vw_doanh_thu_theo_ngay`;
CREATE ALGORITHM=UNDEFINED SQL SECURITY DEFINER VIEW `vw_doanh_thu_theo_ngay` AS SELECT
  DATE(hd.thoi_gian) AS ngay,
  COUNT(hd.id_hoa_don) AS so_hoa_don,
  SUM(hd.tong_tien) AS doanh_thu
FROM hoa_don hd
GROUP BY DATE(hd.thoi_gian)
ORDER BY DATE(hd.thoi_gian) DESC 
;

-- Removing temporary table and create final VIEW structure
DROP TABLE IF EXISTS `vw_hoa_don_chi_tiet`;
CREATE ALGORITHM=UNDEFINED SQL SECURITY DEFINER VIEW `vw_hoa_don_chi_tiet` AS SELECT
  hd.ma_hoa_don,
  hd.thoi_gian,
  nd.ho_ten AS nhan_vien,
  t.ten_thuoc,
  l.so_lo,
  l.han_su_dung,
  cthd.so_luong_ban,
  cthd.don_gia_ban,
  cthd.giam_gia_phan_tram,
  cthd.thanh_tien
FROM hoa_don hd
JOIN nguoi_dung nd ON nd.id_nguoi_dung = hd.id_nhan_vien
JOIN ct_hoa_don cthd ON cthd.id_hoa_don = hd.id_hoa_don
JOIN lo_thuoc l ON l.id_lo_thuoc = cthd.id_lo_thuoc
JOIN thuoc t ON t.id_thuoc = l.id_thuoc 
;

-- Removing temporary table and create final VIEW structure
DROP TABLE IF EXISTS `vw_ton_kho_theo_thuoc`;
CREATE ALGORITHM=UNDEFINED SQL SECURITY DEFINER VIEW `vw_ton_kho_theo_thuoc` AS SELECT
  t.id_thuoc,
  t.ma_vach,
  t.ten_thuoc,
  t.don_vi_tinh,
  SUM(l.so_luong_ton) AS tong_ton_kho
FROM thuoc t
JOIN lo_thuoc l ON l.id_thuoc = t.id_thuoc
GROUP BY t.id_thuoc, t.ma_vach, t.ten_thuoc, t.don_vi_tinh 
;

